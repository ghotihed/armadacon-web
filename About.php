        <p>Other conventions tend to be focused one medium or one subject within that medium. We've found that fans find this format restrictive as most fans like more than one type of medium irrespective of subject matter.</p>
        <h3 align="left">Why do you describe yourselves as 'unconventional'?</h3>
        <p>Many modern conventions have become very commercialised. They're slickly presented and well polished events. This type of convention is now considered the norm. Fans go to these conventions to be entertained by the guests. ArmadaCon is completely different; most attendees come to entertain each other!<br />
