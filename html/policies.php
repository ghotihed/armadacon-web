        <h2>Anti-Harassment Policy</h2>
        <p>ArmadaCon is dedicated to providing a great convention experience for everyone, whatever their gender,
            sexuality, disability, physical appearance, body size, race, religion, or other differences. We will not
            tolerate any form of harassment of convention participants. Con participants found to be engaging in
            harassment may be sanctioned or expelled from the convention without refund.</p>

        <p>Harassment can include (but is not limited to):</p>
        <ul>
            <li>Offensive verbal comments about gender, sexuality, impairment, physical appearance, body size, race or
                religion
            </li>
            <li>Showing sexual imagery in public spaces</li>
            <li>Intimidation, stalking or following</li>
            <li>Photographing/recording someone without their permission</li>
            <li>Sustained disruption of talks or other events</li>
            <li>Uninvited physical contact or sexual attention</li>
        </ul>

        <h3>What should I do if I am being harassed?</h3>
        <p>In some cases you may find the harassment stops if you clearly say 'no', 'please leave me alone' or simply
            walk away. We would appreciate it if the registration desk was informed, to help us identify any repeat
            offenders.</p>

        <p>If you continue to be harassed or notice someone else being harassed; please visit the registration desk or
            contact a member of the convention committee. Committee members can help participants contact the hotel or
            law enforcement (if that's what they'd like to do), provide a listener, or otherwise assist those
            experiencing harassment to feel safe during the Convention.</p>

        <h2>Costumes and Weapons Policy</h2>
        <p>We love costumes at ArmadaCon, and are very much looking forward to seeing all your amazing cosplay! Here are
